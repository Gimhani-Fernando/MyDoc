    private void btnbmiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnbmiMouseClicked
        // TODO add your handling code here:
        float mass,height,bmi;
        mass=Float.parseFloat(txtmass.getText());
        height=Float.parseFloat(txtheight.getText());
        bmi=mass/(height*height);
        txtbmi.setText(String.valueOf(bmi));
        if(bmi>=30)
            txtstatus.setText("Obese");
        else if(bmi>=25 && bmi<30)
            txtstatus.setText("Overweight");
        else if(bmi>=18.5 && bmi<25)
            txtstatus.setText("Normal");
        else 
            txtstatus.setText("Underweight");       
        
    }//GEN-LAST:event_btnbmiMouseClicked

