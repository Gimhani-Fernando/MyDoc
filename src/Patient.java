    private void tab2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tab2MouseClicked
        // TODO add your handling code here:
        jpmedicine.setVisible(true);
        jphome.setVisible(false);
        jphealth.setVisible(false);
        
        String patient_id=txtid.getText();
        try{
            MyDbConnection mycon=new MyDbConnection();
            Statement st=mycon.getMyConnection().createStatement();
            ResultSet rst=st.executeQuery("Select * from patientmedicine where pid='"+patient_id+"'");
            while(rst.next()){
                
                
                String mid=rst.getString("mid");
                String meal=rst.getString("meal");
                String quantity=rst.getString("quantity");
                String morning=rst.getString("morning");
                String afternoon=rst.getString("afternoon");
                String night=rst.getString("night");
                
                ResultSet rst2=st.executeQuery("Select * from medicine where mid='"+mid+"'");
                while(rst2.next()){
                    String mname=rst2.getString("mname");
                    
                
                    String patient__medicine_data[]={mid,mname,meal,quantity,morning,afternoon,night};
                    DefaultTableModel patient_medicine_table=(DefaultTableModel)jTable2.getModel();
                    patient_medicine_table.addRow(patient__medicine_data);
                }
            }          
            
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_tab2MouseClicked

